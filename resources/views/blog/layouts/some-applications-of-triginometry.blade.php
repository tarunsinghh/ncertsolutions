<p><strong><em>2. A tree breaks due to storm and the broken part
bends so that the top of the tree touches the ground
making an angle 30° with it. The distance between
the foot of the tree to the point where the top
touches the ground is 8 m. Find the height of the
tree.</em></strong></p>
<p><strong>Solution:</strong></p>
<p>The tree was initially AB',then it broke and fall to the ground making an angle of 30 with horizintal,clearly we hve to find h + H,as this will give us the total height of the tree</p>
<p><img class="alignnone wp-image-2112" src="/10-0-9-2.png" alt="8" width="400" height="300" /></p>
<p>In \( \triangle CBA \)</p>
<p>tan \(\theta\) = \(\frac{h}{8}  \)</p>
<p>\(\frac{1}{\sqrt{3}}  =     \frac{h}{8}   \)      </p>
<p>\( h  =     \frac{8}{\sqrt{3}} = 10   \)      </p>

<p>sin \(\theta\) = \(\frac{h}{H}  \)</p>
<p>\(\frac{1}{2}  =     \frac{\frac{8}{\sqrt{3}}}{H}   \)      </p>
<p>\( H  =     \frac{16}{\sqrt{3}}    \)      </p>


<p>Total height of the tree  = h + H = \(        \frac{8}{\sqrt{3}} + \frac{16}{\sqrt{3}}   = 8\sqrt{3}    \)</p>



