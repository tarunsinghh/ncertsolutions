<p><strong>Question 1:<br/>
In Δ ABC, right-angled at B, AB = 24 cm, BC = 7 cm. Determine :
(i) sin A, cos A
(ii) sin C, cos C ?</strong></p>
<p><img class="alignnone wp-image-2112" src="/10-0-7-1.png" alt="8" width="400" height="300" /></p>

