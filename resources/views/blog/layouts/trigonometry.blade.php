<div class="azc_tsh_toggle_container" style="border: 1px solid rgb(204, 204, 204); font-size: 14px;margin-left:14px"><br>
<p>Since the triangle is right angled,we will apply pythagoras theorem </p>
<p>\(  AC^{2} = AB^{2} + BC^{2}                        \)</p>
<p>\(  AC^{2} = 24^{2} + 7^{2} = 576 + 49 = 625,AC = 25	\)</p>
<p>sin A = \(  \frac{Side\;opposite\;to\;A}{hypotenuse} = \frac{BC}{AC} = \frac{7}{25} \)</p>
<p>cos A = \( \frac{Side\;adjacent\;to\;A}{hypotenuse} = \frac{AB}{AC} = \frac{24}{25} \)</p>
<p>sin A = \( \frac{Side\;opposite\;to\;C}{hypotenuse} = \frac{AB}{AC} = \frac{24}{25} \)</p>
<p>cos A = \( \frac{Side\;adjacent\;to\;C}{hypotenuse} = \frac{BC}{AC} = \frac{7}{25} \)</p>
</div>
