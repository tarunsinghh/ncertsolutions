<p><strong><em>1.In quadrilateral ACBD, AC = AD and AB bisects ∠ A(see Fig. 7.16). Show that ∆ ABC ≅ ∆ ABD.
What can you say about BC and BD?</em></strong></p>

<p><strong><em>Solution:</em></strong></p>
<p>In ∆ ABC and  ∆ ABD </p>
<p>AC = AD</p>
<p>CAB = DAB</p>
<p>AB = AB (Common)</p>
<p>So by SAS Congruence condition</p>
<p>∆ ABC ≅ ∆ ABD</p>
<p>Since traingles are congruent ,corresponding sides and angles are equal</p>
<p>Hence BC = BD</p>


<p><strong><em>2.ABCD is a quadrilateral in which AD = BC and
∠ DAB = ∠ CBA (see Fig. 7.17). Prove that
(i) ∆ ABD ≅ ∆ BAC
(ii) BD = AC
(iii) ∠ ABD = ∠ BAC.</strong></em>
<p><strong><em>Solution:</em></strong></p>
<p>This is simple too:</p>
<p>In ∆ ABD and  ∆ BAC</p>
<p>AD = BC (Given)</p>
<p>DAB = CAB (Given)</p>
<p>AB = AB (Common)</p>
<p>So by SAS congruene condition:</p>
<p>∆ ABC ≅ ∆ ABD</p>
