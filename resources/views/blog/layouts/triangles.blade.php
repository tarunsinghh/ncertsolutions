<p><strong><em>6. In Fig. 7.21, AC = AE, AB = AD and
∠ BAD = ∠ EAC. Show that BC = DE.</strong></em></p>
<p><strong><em>Solution:</em></strong></p>
<p>We are given two things but need a third thing too to prove triangles are congruent</p>
<p>∠ BAD = ∠ EAC</p>
<p>∠ BAD + ∠ DAC  = ∠ EAC + ∠ DAC</p>
<p>∠ BAC  = ∠ DAE </p>
<p>In ∆ ABC and  ∆ ADE</p>
<p>AB = AD ()</p>
<p>∠ BAC  = ∠ DAE </p>
<p>AC= AE (common)</p>
<p>so by SAS congruence condition,∆ ABC ≅ ∆ ADE</p>
<p>so BC = DE (by C.P.C.T)</p>



