<p><strong><em>1.In quadrilateral ACBD, AC = AD and AB bisects ∠ A(see Fig. 7.16). Show that ∆ ABC ≅ ∆ ABD.
What can you say about BC and BD?</em></strong></p>

<p><strong><em>Solution:</em></strong></p>
<p>In ∆ ABC and  ∆ ABD </p>
<p>AC = AD</p>
<p>CAB = DAB</p>
<p>AB = AB (Common)</p>
<p>So by SAS Congruence condition</p>
<p>∆ ABC ≅ ∆ ABD</p>
<p>Since traingles are congruent ,corresponding sides and angles are equal</p>
<p>Hence BC = BD</p>


