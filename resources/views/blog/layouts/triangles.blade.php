<p><strong>Solution:</strong></p>
<p>In ∆ BMB and ∆ DMC , we have</p>
<p>(i) DM = CM (given)</p>
<p>BM = AM (M is the midpoint of AB)</p>
<p>∠ DMB = ∠ AMC (Vertically opposite angles)</p>
<p>∆ AMC = ∆ BMD (By SAS)</p>
<p>Hence Proved.</p>
<p>(ii) AC || BD ( ∠ DBM\ and\ ∠ CAM are .alternate angles)</p>
<p> ∠ DBC + ∠ ACB = \( 180^{o}\) (Sum of co-interior angles)</p>
<p>∠ DBC = \( 90^{o}\)</p>
<p>Hence proved.</p>
<p>(iii) In ∆ DBC and ∆ ACB , We have</p>
<p>DB = AC (CPCT)</p>
<p>BC = BC (Common)</p>
<p>∠ DBC = ∠ ACB (Each = \( 90^{o}) \) )</p>
<p>∆ DBC = ∆ ACB  (By SAS)</p>
<p>Hence proved.</p>
<p>(iv) AB = CD</p>
<p>\( \frac{1}{2} AB = \frac{1}{2} CD\) (CPCT)</p>
<p>Hence, \(\frac{1}{2} AB = CM\) Proved.</p>



