<p>Hence,by CPCT</p>
<p> BD = AC</p>
<p>∠ ABD = ∠ BAC</p>





<p><strong><em>3. AD and BC are equal perpendiculars to a line
segment AB (see Fig. 7.18). Show that CD bisects
AB.</strong></em>
<p><strong><em>Solution:</em></strong></p>
<p>This is simple too:</p>
<p>In ∆ BOC and  ∆ AOD</p>
<p>CBO = DAO (Given)</p>
<p>BOC = AOD (Vertically oposite angles)</p>
<p>BC = AD (Given)</p>
<p>So by AAS congruene condition:</p>
<p>∆ BOC ≅ ∆ AOD</p>
<p>Since triangles are congruent ,corresponding sides and angles will be  equal</p>
<p>Hence,by CPCT</p>
<p> CO = OD</p>
<p> BO = OA </p>
<p>Since the intersection point of both lines O divided AB and  CD into 2 equal parts</p>
<p>This means CD bisects AB</p>
<p><strong<em>Note:Only prooving CO = OD or BO = OA doesnt not imply that CD bisects AB,we need to write both CO = OD AND OB = OA </em></strong</p>



<p><strong><em>4. l and m are two parallel lines intersected by
another pair of parallel lines p and q
