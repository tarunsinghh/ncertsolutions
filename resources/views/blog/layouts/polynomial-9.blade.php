<p><strong>Question 3:<br/>
Verify whether the following are zeroes of the polynomial, indicated against them.
<p>(i) p(x) = 3x + 1, x = \( \frac{–1}{3} \) </p>
<p>(ii) p(x) = 5x – π, x = \( \frac{4}{5} \) </p>
<p>(iii) p(x) = \( x^{2} – 1, x = 1, –1 \)</p>
<p>(iv) p(x) = (x + 1) (x – 2), x = – 1, 2</p>
<p>(v) p(x) = x^{2},x = 0 </p> 
<p>(vi) p(x) = lx + m, x = \( \frac{–m}{l} \) </p>
<p>(vii)p(x) = 3x^{2} - 1, x = \( \frac{–1}{\sqrt{3}},  \frac{2}{\sqrt{3}} \)
<p>(viii) p(x) = 2x + 1, x = 2 ?</strong></p>
<h3 class="azc_tsh_toggle" style="border: 1px solid #cccccc; background-color: #f2f2f2; background-image: none !important; padding-left: 10px; "><a href="#" style="font-size: 14px; 14px">Show Answer</a></h3>
<div class="azc_tsh_toggle_container" style="border: 1px solid rgb(204, 204, 204); font-size: 14px;margin-left:14px"><br>

<p>i) For, \(x = -\frac{1}{3}\)</p>
<p>\(p(x) = 3x + 1\\ ∴ p(-\frac{1}{3}) = 3(-\frac{1}{3}) + 1=-1+1=0\)</p>
<p>\(∴ -\frac{1}{3}\) is a zero of p(x).</p>
<br>
<p><strong><em> </em></strong><strong><em>(ii) \(p(x) = 5x &#8211; \pi , x = \frac{4}{5}\)</em></strong></p>
<p>For, \(x = \frac{4}{5}\)</p>
<p>\(p(x) = 5x &#8211; \pi \\ ∴ p(\frac{4}{5}) = 5(\frac{4}{5}) &#8211; \pi=4-\pi\)</p>
<p>\(∴ \frac{4}{5}\) is not a zero of p(x).</p>
<br>
<p><strong><em> </em></strong><strong><em>(iii) \(p(x) = x^{2}- 1, x = 1, -1\)</em></strong></p>
<p>For, \(x = 1,-1\)</p>
<p>\(p(x) = x^{2}- 1\\ ∴ p(1) = x^{1}- 1=1-1=0\\ p(-1) = x^{-1}- 1=1-1=0\)</p>
<p>\(∴ 1,-1\) are zeros of p(x).</p>
<p>&nbsp;</p>
<p><strong><em>(iv)</em></strong> <strong><em>\(p(x) = (x + 1) (x &#8211; 2), x = -1, 2\)</em></strong></p>
<p>For, \(x = -1,2\)</p>
<p>\(p(x) = (x + 1) (x &#8211; 2)\\ ∴ p(-1) = (-1 + 1) (-1 &#8211; 2)\\ =((0)(-3))\\ =0\\ \\ p(2) = (2 + 1) (2 &#8211; 2)\\ =(3)(0)=0\)</p>
<p>\(∴ -1,2\) are zeros of p(x).</p>


<p><strong><em>(v) \(p(x) = x^{2}, x = 0\)</em></strong></p>
<p>For, \(x = 0\)</p>
<p>\(p(x) = 0^{2}=0\)</p>
<p>\(∴ 0\) is a  zero of p(x).</p>
<p>&nbsp;</p>
<p><strong><em>(vi)</em></strong> <strong><em>\(p(x)=lx+m,x=-\frac{m}{l}\)</em></strong></p>
<p>For, \( x=-\frac{m}{l} \)</p>
<p>\(p(x)=lx+m\\ ∴ p(-\frac{m}{l} )=l(-\frac{m}{l} )+m=-m+m=0\)</p>
<p>\(∴-\frac{m}{l}\) is a zero of p(x).</p>


<p><strong><em>(vii)</em></strong> <strong><em>\(p(x)=3x^{2}-1,x=-\frac{1}{\sqrt{3}},\frac{2}{\sqrt{3}}\)</em></strong></p>
<p>For, \( x=-\frac{1}{\sqrt{3}},\frac{2}{\sqrt{3}} \)<br />
<p>\(p(x)=3x^{2}-1\\ ∴ p(-\frac{1}{\sqrt{3}})=3(-\frac{1}{\sqrt{3}})^{2}-1\\ =3(\frac{1}{3})-1=1-1=0\\ \\ ∴ p(\frac{2}{\sqrt{3}})=3(\frac{2}{\sqrt{3}})^{2}-1\\ =3(\frac{4}{3})-1=4-1=3\neq 0\)</p>
<p>\(∴ -\frac{1}{\sqrt{3}}\) is a zero of p(x) but \(\frac{2}{\sqrt{3}}\) is not a zero of p(x).</p>



<p><strong><em>(viii) \(p(x) = 2x + 1, x = \frac{1}{2}\)</em></strong><strong><em><br />
<p>For, \( x=\frac{1}{2} \)</p>
<p>\( p(x) = 2x + 1\\ ∴ p(\frac{1}{2} ) = 2(\frac{1}{2}) + 1=1+1=2\neq 0\)
<p>\(∴\frac{1}{2}  \) is not a  zero of p(x).</p>


