<p><strong>Question 5:<br/>
5. A cubical box has each edge 10 cm and another cuboidal box is 12.5 cm long, 10 cm
wide and 8 cm high.
(i) Which box has the greater lateral surface area and by how much?
(ii) Which box has the smaller total surface area and by how much?. </strong></p>
<h3 class="azc_tsh_toggle" style="border: 1px solid #cccccc; background-color: #f2f2f2; background-image: none !important; padding-left: 10px; "><a href="#" style="font-size: 14px; 14px">Show Answer</a></h3>
<div class="azc_tsh_toggle_container" style="border: 1px solid rgb(204, 204, 204); font-size: 14px;margin-left:14px"><br>
<p>Lateral surface area doesnt include top and bottom </p>
<p>For cube,lateral surface area = \(  4a^{2} = 4\times 10^{2} = 400 cm^{2}   \)</p>
<p>For cuboid,lateral surface area = \( 2(12.5\times 8 + 10\times 8) = 360 cm^{2}                           \)</p>
<p>So cube has more lateral surface area by 40 \( cm^{2}         \)</p>
<p>Total surface area of cube = \(   6a^{2} = 600 cm^{2}           \)                       </p>
<p>Total surface area of cuboid = \(  2(12.5\times 10 + 10\times 8 + 8\times 12.5) = 2(125 + 80 + 100) = 2(210 + 100) = 620 cm^{2}                 \)</p>
<p>Cuboid has more total surface area by 620 - 600 = 20 \( cm^{2}  \)</p>
</div>
