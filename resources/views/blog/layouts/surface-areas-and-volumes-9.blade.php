<p><strong>Question 1:<br/>
A plastic box 1.5 m long, 1.25 m wide and 65 cm deep is to be made. It is opened at the
top. Ignoring the thickness of the plastic sheet, determine:
(i) The area of the sheet required for making the box.
(ii) The cost of sheet for it, if a sheet measuring \( 1m^{2} \)costs Rs 20. </strong></p>
<h3 class="azc_tsh_toggle" style="border: 1px solid #cccccc; background-color: #f2f2f2; background-image: none !important; padding-left: 10px; "><a href="#" style="font-size: 14px; 14px">Show Answer</a></h3>
<div class="azc_tsh_toggle_container" style="border: 1px solid rgb(204, 204, 204); font-size: 14px;margin-left:14px"><br>
<p>Since the box is open at the top we only have 5 faces to calculate area</p>
<p>So,Area = lb + 2lh + 2 bh                             </p>
<p>=\(   1.5\times 1.25 + 2(1.5\times .65 ) + 2 (1.25\times .65)                    \)</p>
<p>=\(   1.875 +  1.95 + 1.625 = 5.45 m^{2} \) </p>
<p>Cost of sheet = \(  20\times 5.45 = 109 Rs             \)           </p>                                                                        
