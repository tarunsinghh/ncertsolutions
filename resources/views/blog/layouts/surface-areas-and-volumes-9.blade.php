<p><strong>Question 7:<br/>
Shanti Sweets Stall was placing an order for making cardboard boxes for packing
their sweets. Two sizes of boxes were required. The bigger of dimensions
25 cm × 20 cm × 5 cm and the smaller of dimensions 15 cm × 12 cm × 5 cm. For all the
overlaps, 5% of the total surface area is required extra. If the cost of the cardboard is
4 for 1000 cm 2 , find the cost of cardboard required for supplying 250 boxes of each
kind.</strong></p>
<h3 class="azc_tsh_toggle" style="border: 1px solid #cccccc; background-color: #f2f2f2; background-image: none !important; padding-left: 10px; "><a href="#" style="font-size: 14px; 14px">Show Answer</a></h3>
<div class="azc_tsh_toggle_container" style="border: 1px solid rgb(204, 204, 204); font-size: 14px;margin-left:14px"><br>
<p>Surface area of  bigger box =  2(25\times 20 + 20\times 5 + 5\times 25) = 2(500 + 100 + 125) = 2(725) = 1450 cm^{2}      </p>
<p>Surface area of smaller box =  2(15\times 12 + 12\times 5 + 5\times 15) = 2(180 + 60 + 75) = 2(315) = 630 cm^{2}          </p>
<p>Total surface area of bigger box including 5% extra = \(   1450 + .05\times 1450 = 1450 + 72.5 = 1522.5 cm^{2}  \) </p>
<p>Total surface area of smaller boxe including 5% extra = \( 630 + .05\times 630 = 630 + 31.5 = 661.5 cm^{2}  \) </p>
<p>So Total surface area of 250 bigger and small boxes put together
= \(     250\times 1522.5 + 250\times 661.5 = 546000 cm^{2}  \)
<p>Cost of cardboard for 1000 \( cm^{2}  \) = 4 </p>
<p>Cost of cardboard for 1 \( cm^{2}   \) = \( .004\times 546000 = 2184 Rs \)</p>
<p>Total cost required for 250 smaller and bigger boxes = 2184 Rs </p>
</div>
